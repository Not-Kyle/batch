public class Main {
    public static void main(String[] args) {
        
        for (int index = 0; index < 11; index++) {
            System.out.println(index);
        }
        
    }
}

// Decrease

public class Main {
    public static void main(String[] args) {

        for (int index = 11; index > 0; index--) {
            System.out.println(index);
        }

    }
}
