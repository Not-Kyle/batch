// Must use #include <cmath>

// -- > Max  and Min < -- \\

  #include <iostream>
  #include <cmath>
  using namespace std;
  
  int main() {
  	int x = 7, y = 10;
  	cout << max(x, y) << endl;
  	cout << min(x, y);
  	return 0;
  }

// -- > Log, Sqrt, Round < -- \\

  #include <iostream>
  #include <cmath>
  using namespace std;
  
  int main() {
  	int x = 7, y = 10;
  	float z = 4.3;
  	cout << log(x) << std::endl; // logarithm
  	cout << sqrt(y) << std::endl;
  	cout << round(z) << std::endl;
  	return 0;
  }

