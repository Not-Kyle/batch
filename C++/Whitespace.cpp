// Code or variables can be defined on the next line

#include <iostream>
using namespace std;

int main() {

	cout
		<< "YES";

	const int X
		= { 5 };

	cout
		<< X;

	return 0;
}

// Or, Code can be spacedout

#include <iostream>
using namespace std;

int main() {

	cout << "YES";

	const int X = { 5 };

	cout << X;

	return 0;
}

